class Player():
    
    def __init__(self,x,y):
        img = pygame.image.load('personagem.png')
        self.image = pygame.transform.scale(img, (40,80))
        self.rect = self.image.get_rect()
        self.rect.x = x
        self.rect.y = y 
        self.largura =  self.image.get_width()
        self.altura =  self.image.get_height()
        self.vel_y = 0
        self.jumped = False
    
    def update(self):
        dx = 0
        dy = 0

        #get keypresses(tá dando errado o K_LEFT e K_RIGHT não sei)
        key = pygame.key.get_pressed()
        if key[pygame.K_SPACE] and self.jumped == False:
            self.vel_y = -15
            self.jumped = True
        if key[pygame.K_SPACE] == False:
            self.jumped = False
        if key[pygame.K_LEFT]:
            dx -= 5
        if key[pygame.K_RIGHT]:
            dx += 5


        #add gravity
        self.vel_y += 1
        if self.vel_y > 10:
            self.vel_y = 10
        dy += self.vel_y

        #check colision
        for tile in mundo.lista_bloco:
            
            #check for colision in x direction
            if tile[1].colliderect(self.rect.x + dx, self.rect.y, self.largura, self.altura):
                dx = 0
            #check colision y direction
            if tile[1].colliderect(self.rect.x, self.rect.y + dy, self.largura, self.altura):
                #check if below the ground i. e. jumping
                if self.vel_y < 0:
                    dy = tile[1].bottom - self.rect.top
                    self.vel_y = 0

                #check ifa above the groutnd i. e. falling
                elif self.vel_y >= 0:
                    dy = tile[1].bottom - self.rect.bottom
                    self.vel_y = 0


        #update player coordinates
        self.rect.x += dx
        self.rect.y += dy

        if self.rect.bottom > screen_altura:
            self.rect.bottom = screen_altura
            dy = 0
 
        #draw player onto screen
        tela.blit(self.image, self.rect)
        pygame.draw.rect(tela, (255,255,255), self.rect, 2)

#classe que armazena e constroi o mapa atraves dos blocos
class World():
    
    def __init__(self, data):
        self.lista_bloco = []
        #carregar imagem
        varandinha = pygame.image.load("bloco.png")
        linha_count = 0
        for linha in data:
            col_count = 0
            for bloco in linha:
                if bloco == 1:
                    img = pygame.transform.scale(varandinha, (tamanho_bloco, tamanho_bloco))
                    img_rect = img.get_rect()
                    img_rect.x = col_count * tamanho_bloco
                    img_rect.y = linha_count * tamanho_bloco
                    bloco = (img, img_rect)
                    self.lista_bloco.append(bloco)
                col_count += 1
            linha_count += 1

    def draw(self):
        for bloco in self.lista_bloco:
            tela.blit(bloco[0],bloco[1])
            pygame.draw.rect(tela,(255,255,255), bloco[1], 1)
